<?php

  abstract class Teste {

    public static function testandoClasse() {
      echo "Este método é de uma classe abstrata <br>";
    }

    abstract public function testeAbs();

  }

  Teste::testandoClasse(); // Este método é de uma classe abstrata

  class Nova extends Teste {

    public function testeAbs() {
      echo "Teste método abstrato <br>";
    }

  }

  $n = new Nova; // Este método é de uma classe abstrata
  $n->testeAbs(); // Teste método abstrato